1 now() 返回当前时间 格式:日期 时间

    select now();
    +---------------------+
    | now()               |
    +---------------------+
    | 2020-10-05 20:33:57 |
    +---------------------+


2 current_timestamp()  返回时间戳(日期+时间)

    select current_timestamp();
    +---------------------+
    | current_timestamp() |
    +---------------------+
    | 2020-10-05 20:34:55 |
    +---------------------+

  unix_timestamp()  返回真实时间戳

    select unix_timestamp();
    +------------------+
    | unix_timestamp() |
    +------------------+
    |       1601902117 |
    +------------------+
  from_unixtime(秒数)  将秒数转换成真实时间

    select from_unixtime(1234567890);
    +---------------------------+
    | from_unixtime(1234567890) |
    +---------------------------+
    | 2009-02-14 07:31:30       |
    +---------------------------+


3 curdate()  current_date() 返回当前日期
    select curdate();
    select current_date();   两个都显示相同

    +------------+
    | curdate()  |
    +------------+
    | 2020-10-05 |
    +------------+


4 curtime() current_time()  返回当前时间
    select curtime();
    select current_time();   两个都显示相同

    +-----------+
    | curtime() |
    +-----------+
    | 20:34:43  |
    +-----------+


5 date()  从日期或日期时间中提取日期
    select date('2018-01-01 15:34:43');  显示 2018-01-01


6 time()  从日期或日期时间中提取时间
    select time('2018-01-01 11:45:08');  显示 11:45:08


7 adddate(d,n) 计算起始日期加上n填的日期
    select adddate('2018-01-01',4);
    select adddate('2018-01-01',interval 4 day);  两个显示 2018-01-05

    select adddate('2018-01-01 20:01:55',4);      显示 2018-01-05 20:01:55


8 addtime(t,n) 时间t加上n秒
    select addtime('11:11:11',4);                 显示 11:11:15
    select addtime('2018-01-01 11:11:11',4);      显示 2018-01-01 11:11:15


9 date_add(日期,interval 时间数字 type);  进行时间的增加
    type: day/hour/minute/second

        select date_add('2018-01-01',interval 5 day),date_add('2018-01-01',interval 5 minute);
        +---------------------------------------+------------------------------------------+
        | date_add('2018-01-01',interval 5 day) | date_add('2018-01-01',interval 5 minute) |
        +---------------------------------------+------------------------------------------+
        | 2018-01-06                            | 2018-01-01 00:05:00                      |
        +---------------------------------------+------------------------------------------+


10 datediff('新日期','旧日期') 判断两个日期之间的天数差距  参数日期必须使用字符串包裹
    select datediff('2018-01-01','2018-02-01');
    +-------------------------------------+
    | datediff('2018-01-01','2018-02-01') |
    +-------------------------------------+
    |                                 -31 |
    +-------------------------------------+

    select datediff('2018-02-01','2018-01-01');
    +-------------------------------------+
    | datediff('2018-02-01','2018-01-01') |
    +-------------------------------------+
    |                                  31 |
    +-------------------------------------+

    select datediff('2018-01-01 11:11:11','2018-01-01 00:11:11');
    +-------------------------------------------------------+
    | datediff('2018-01-01 11:11:11','2018-01-01 00:11:11') |
    +-------------------------------------------------------+
    |                                                     0 |   //只计算天数,不计算其他
    +-------------------------------------------------------+
